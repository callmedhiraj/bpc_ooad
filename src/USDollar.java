public abstract class USDollar {
    private double amount;

    public USDollar() {
    }

    public USDollar(double amount) {
        this.amount = amount;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }
    
    public abstract double convertToNrs();
    
}
